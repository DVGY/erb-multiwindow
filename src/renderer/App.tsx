import { MemoryRouter as Router, Routes, Route } from 'react-router-dom';
import icon from '../../assets/icon.svg';
import './App.css';

const Hello = () => {
  const handleInstantiateSecondWindow = () => {
    window.electron.ipcRenderer.sendMessage('ipc-second-window', ['ping']);
  };

  return (
    <div>
      <div className="Hello">
        <img width="200" alt="icon" src={icon} />
      </div>
      <h1>electron-react-boilerplate</h1>
      <div className="Hello">
        <button type="button" onClick={handleInstantiateSecondWindow}>
          <span role="img" aria-label="folded hands">
            🙏
          </span>
          Open Second Window
        </button>
      </div>
    </div>
  );
};

export default function App() {
  return (
    <Router>
      <Routes>
        <Route path="/" element={<Hello />} />
      </Routes>
    </Router>
  );
}
